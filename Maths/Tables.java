// To print Tables of a given number

public class Tables {
    public static void main(String args[]) {
        int n, i;
        n = Integer.parseInt(args[0]);
        for(i=1;i<=10;i++) // Calculating upto 10
         System.out.println(i+" * "+n+" = "+i*n);
    }
    
}
